# age.rb - input age output age in 10, 20, 30, 40 years

puts "Input your current age and press [enter]"
age = gets.chomp.to_i

puts "In 10 years you will be:"
puts age + 10
puts "in 20 years you will be:"
puts age + 20
puts "In 30 years you will be:"
puts age + 30
puts "In 40 years you will be:"
puts age + 40
